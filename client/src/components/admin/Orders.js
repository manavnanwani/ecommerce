const PropertyList = ({ i, property }) => {
  const classes = useStyles();
  return (
      <>
        <ListItem onClick={() => window.location.href = `/admin/orderinfo?id=${property.userId}`} button>
            <ListItemText primary={`Order ${i}`} /><Chip
                label="Not Completed"
                style={{ marginLeft: '4px', backgroundColor: 'red', color: '#fff' }}
            /> 
        </ListItem>
        <Divider />
      </>
  )
}


export default function Orders({ location }) {
  const classes = useStyles();
  const [open, setOpen] = React.useState(true);

  const [allproperties, setAllproperties] = React.useState({});
  const [loading, setLoading] = React.useState(true);

  React.useEffect(() => {
    axios.get(`${API_SERVICE}/api/v1/main/getallorders`)
      .then(response => {
          setAllproperties(response.data);
          setLoading(false);
      })
  }, []);

  const handleDrawerOpen = () => {
    setOpen(true);
  };
  const handleDrawerClose = () => {
    setOpen(false);
  };
  const fixedHeightPaper = clsx(classes.paper, classes.fixedHeight);

  const showPropertyList = () => {
      var i = 0; 
      return allproperties.map(property => {
          i = i + 1;
          return <PropertyList i={i} property={property} key={property._id}  />
      })
  }

  return (
    <div className={classes.root}>
      <CssBaseline />
      <AppBar position="absolute" className={clsx(classes.appBar, open && classes.appBarShift)}>
        <Toolbar className={classes.toolbar}>
          <IconButton
            edge="start"
            color="inherit"
            aria-label="open drawer"
            onClick={handleDrawerOpen}
            className={clsx(classes.menuButton, open && classes.menuButtonHidden)}
          >
            <MenuIcon />
          </IconButton>
          <Typography component="h1" variant="h6" color="inherit" noWrap className={classes.title}>
            Dashboard
          </Typography>
          
        </Toolbar>
      </AppBar>
      <Drawer
        variant="permanent"
        classes={{
          paper: clsx(classes.drawerPaper, !open && classes.drawerPaperClose),
        }}
        open={open}
      >
        <div className={classes.toolbarIcon}>
          <IconButton onClick={handleDrawerClose}>
            <ChevronLeftIcon />
          </IconButton>
        </div>
        <Divider />
        <List>{mainListItems}</List>
      </Drawer>
      <main className={classes.content}>
        <div className={classes.appBarSpacer} />
        <Container maxWidth="lg" className={classes.container}>
            <h2 style={{ marginBottom: '4px' }}>
                Orders
            </h2>
            <List component="nav" className={classes.listroot} aria-label="mailbox folders">
            {
                loading === true ? (
                    <center style={{ marginTop: '10%' }}>
                        <CircularProgress />
                    </center>
                ) : (
                    <>
                    {showPropertyList()}
                    </>
                )
            }
            </List>
          <Box pt={4}>
            <Copyright />
          </Box>
        </Container>
      </main>
    </div>
  );
}